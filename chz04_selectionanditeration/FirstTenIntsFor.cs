using System;


namespace chz04_selectionanditeration
{
    /// <summary>
    /// C# program that displays the first ten intergers using a for loop
    /// </summary>
    class FirstTenIntsFor
    {
        static void Main(string[] args)
        {
            for (int e = 1; e<= 10; e++)
            {
                Console.WriteLine($"{e}");
            }
        }
    }
}
